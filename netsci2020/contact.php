  <script>
    $("form#contact_form").submit(function(e){
    e.preventDefault();
   var fname=$("#fname").val(),
     lname=$("#lname").val(),    
    email=$("#email").val(),
     subject=$("#subject").val(),
      message=$("#message").val();
    $.ajax({
      url:"contactmail.php",
      method:"POST",
      data:{fname:fname,lname:lname,email:email,subject:subject,message:message},
      success:function(data){
        //alert(data);
        if($.trim(data)=="Success"){
          $("#succ").show();
          $("#contact_form")[0].reset();
          setTimeout(
            function () {
               $("#succ").hide();
            },1000);
        }
        else{
          $("#error").show();
          setTimeout(
            function () {
               $("#error").hide();
            },1000);
        }
      },
    });
  });
